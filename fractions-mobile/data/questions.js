/**
 * Shuffle function for arrays
 * @param {Array} array - Array to shuffle
 * @returns {Array} Shuffled array
 */
export const shuffleArray = (array) => {
  const shuffled = [...array];
  for (let i = shuffled.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [shuffled[i], shuffled[j]] = [shuffled[j], shuffled[i]];
  }
  return shuffled;
};

/**
 * Get shuffled questions for Level 2 Stage 2
 * @param {Object} baseQuestions - Base questions object
 * @returns {Object} Questions with shuffled answers for Level 2 Stage 2
 */
export const getProcessedQuestions = (baseQuestions) => {
  const processedQuestions = { ...baseQuestions };

  // For Level 2 Stage 2, shuffle answers and update correctAnswer
  if (processedQuestions[2] && processedQuestions[2][2]) {
    processedQuestions[2][2].forEach((quiz) => {
      const originalAnswers = [...quiz.answers];
      const originalCorrectIndex = quiz.correctAnswer;
      const shuffled = shuffleArray(originalAnswers);
      quiz.answers = shuffled;
      const correctString = originalAnswers[originalCorrectIndex];
      quiz.correctAnswer = shuffled.indexOf(correctString);
    });
  }

  return processedQuestions;
};
